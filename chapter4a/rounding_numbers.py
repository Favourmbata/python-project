number = 13.56449
rounded_number = round(number)
print("Round to the nearest integer:",rounded_number)

rounded_tenths = round(number,1)
print("Rounded to the nearest tenths",rounded_tenths)

rounded_hundrendths = round(number,2)
print("Rounded to the nearest hundrenth",rounded_hundrendths)

rounded_thousandths = round(number,3)
print("Rounded to the next thousand",rounded_thousandths)